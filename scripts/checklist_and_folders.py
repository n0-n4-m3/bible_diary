# Генерация таблиц по новому шаблону с пустой строкой под номерами глав
def generate_template_table(books, columns=10):
    result = []
    for book, chapters in books:
        result.append(f"## {book}")
        header = "| " + " | ".join([" "] * columns) + " |"
        separator = "| " + " | ".join(["---"] * columns) + " |"

        result.append(header)
        result.append(separator)

        for i in range(0, chapters, columns):
            row = [str(ch) for ch in range(i + 1, min(i + columns + 1, chapters + 1))]
            row_line = "| " + " | ".join(f"{n:<3}" for n in row) + " |"
            empty_line = "| " + " | ".join(["   "] * len(row)) + " |"

            result.append(row_line)
            result.append(empty_line)

        result.append("")  # пустая строка между книгами
    return "\n".join(result)

